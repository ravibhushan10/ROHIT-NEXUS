// type conversion into string

// number to string
let a=32;
console.log(String(a));
console.log(typeof String(a));

// boolean into string
let az=true;
console.log(String(az)); /* "true" */
console.log(typeof String(az));

// type conversion into boolean
let str="ravi3443";
console.log(Boolean(str));
// it gives always true untill the string is empty
// space is also a character so it gives true
console.log(typeof Boolean(str));